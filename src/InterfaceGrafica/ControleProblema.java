    public void atualiza()
    {
        try {
            con.conecta();
            con.stm=con.con.prepareStatement(sqlPesquisa,ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            con.stm.setInt(1, cod);
            con.rs=con.stm.executeQuery();
            con.rs.first();
            descricao.setText(con.rs.getString("descricao"));
            situacao.setText(con.rs.getString("ativo"));
            con.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }
