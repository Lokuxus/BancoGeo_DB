    public void preencherTabela(String SQL, String[] col) {
        ArrayList dados = new ArrayList();
        String[] colunas = col;
        
        try {
            con.conecta();
            con.stm = con.con.prepareStatement(SQL+this.cod,ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            con.rs = con.stm.executeQuery();
            double c=0;
            con.rs.first();
            do {
                c=c+con.rs.getDouble("custo");
                dados.add(new Object[]{con.rs.getInt("cod_acao"), con.rs.getString("descricao"), con.rs.getDate("dt_execucao"),con.rs.getDouble("custo")});
            } while (con.rs.next());
            con.desconecta();
            vl_total.setText(Double.toString(c));
        } catch (SQLException e) {
               
        }

        ModeloTabela tabela = new ModeloTabela(dados, colunas);
        Dados.setModel(tabela);
        con.desconecta();
    }
    
