    private void salvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_salvarMouseClicked
        try
        {
            con.conecta();
            con.stm = con.con.prepareStatement("insert into tb_problema (dt_inicio,descricao,id_categoria,dt_termino,ativa) values (?,?,?,?,?)");
            String i = (String)categoria.getSelectedItem();
            int in=i.lastIndexOf(":");
            in=Integer.valueOf(i.substring(in+1));
            con.stm.setString(1, dt_abertura.getText());
            con.stm.setString(2, descricao.getText());
            con.stm.setInt(3, in);
            con.stm.setString(4, "00/00/0000");
            con.stm.setBoolean(5, false);
            con.stm.execute();
            con.desconecta();
            JOptionPane.showMessageDialog(null, "Problema incluido com sucesso");
            descricao.setText("");
            dt_abertura.setText("");
        }
        catch(SQLException | NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Erro ao inserir problema : "+e.getMessage());
        }
    }//GEN-LAST:event_salvarMouseClicked

