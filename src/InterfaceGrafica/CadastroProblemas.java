    public void atualizaCombo()
    {
        try {
            con.conecta();
            con.stm = con.con.prepareStatement("select * from tb_categoria order by id_categoria",ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            con.rs=con.stm.executeQuery();
            con.rs.first();
            categoria.removeAllItems();
            do{
                categoria.addItem(new String(con.rs.getString("nome")+":"+con.rs.getInt("id_categoria")));
            }while(con.rs.next());
            con.desconecta();
        } catch (SQLException ex) {
            Logger.getLogger(CadastroProblemas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
